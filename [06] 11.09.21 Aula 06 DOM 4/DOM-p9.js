function MostraOpcoes (nomeArea){
	areaHDI.style.display="none"
	areaHDE.style.display="none"
	areaSSD.style.display="none"
	
	if(nomeArea=="areaHDI") { areaHDI.style.display="block"}
	if(nomeArea=="areaHDE") { areaHDE.style.display="block"}
	if(nomeArea=="areaSSD") { areaSSD.style.display="block"}
}